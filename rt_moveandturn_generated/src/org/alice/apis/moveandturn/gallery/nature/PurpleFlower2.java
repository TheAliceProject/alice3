package org.alice.apis.moveandturn.gallery.nature;
	
public class PurpleFlower2 extends org.alice.apis.moveandturn.gallery.GalleryModel {
	public PurpleFlower2() {
		super( "Nature/PurpleFlower2" );
	}
	public enum Part {
		Petal1( "petal1" ),
		Leaf1( "leaf1" ),
		Center( "center" ),
		Stem2( "stem2" ),
		Petal2( "petal2" ),
		Petal3( "petal3" ),
		Petal4( "petal4" );
		private String[] m_path;
		Part( String... path ) {
			m_path = path;
		}
		public String[] getPath() {
			return m_path;
		}
	}
	public org.alice.apis.moveandturn.Model getPart( Part part ) {
		return getDescendant( org.alice.apis.moveandturn.Model.class, part.getPath() );
	}

}
