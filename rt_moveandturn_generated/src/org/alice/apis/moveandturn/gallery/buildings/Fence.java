package org.alice.apis.moveandturn.gallery.buildings;
	
public class Fence extends org.alice.apis.moveandturn.gallery.GalleryModel {
	public Fence() {
		super( "Buildings/fence" );
	}
	public enum Part {
		Post1( "post1" ),
		Post10( "post10" ),
		CrossPost( "cross post" ),
		Post2( "post2" ),
		Post3( "post3" ),
		Post4( "post4" ),
		Post5( "post5" ),
		Post6( "post6" ),
		Post9( "post9" ),
		Post8( "post8" ),
		Post7( "post7" );
		private String[] m_path;
		Part( String... path ) {
			m_path = path;
		}
		public String[] getPath() {
			return m_path;
		}
	}
	public org.alice.apis.moveandturn.Model getPart( Part part ) {
		return getDescendant( org.alice.apis.moveandturn.Model.class, part.getPath() );
	}

}
